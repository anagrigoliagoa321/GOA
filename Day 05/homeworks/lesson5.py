num1 = int(input('Enter number: '))
num2 = int(input('Enter number: ')) 

print(num1 + num2)
print(num1 - num2)
print(num1 / num2) # 1 დახრილი ხაზიტ გაყოფა წილადს(float). მაგალითად:(10 / 5 == 2.0)
print(num1 // num2)#// ორი დახრილი ხაზით გაყოფა გვაძლევს მთელ რიცხვს(integer)
print(num1 * num2)# * იგივეა რაც გამრავლება
print(num1 > num2)# > მეტია
print(num1 < num2)#< ნაკლებია
print(num1 >= num2)# >= მეტია ან ტოლი
print(num1 <= num2)# <= ნაკლებია ან ტოლი
